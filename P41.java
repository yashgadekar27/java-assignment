 class P41 {
     public static void main(String args[]){
        //volume of cylinder = pi*r*r*h;
        double pi = 3.14;
        int d = 30; //diameter
        int r = d/2; //radius
        int h = 50;
        double volume = pi * r * r * h;
        System.out.println(volume);
    }
}
